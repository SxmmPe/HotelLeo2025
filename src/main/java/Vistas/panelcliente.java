    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
       btnguardar.setText("Editar");
    habilitar();
    btneliminar.setEnabled(true);
    accion = "editar";

    int fila = tablalistado.getSelectedRow();
    if (fila >= 0 && fila < listaClientes.size()) {
        cliente c = listaClientes.get(fila);

        txtidpersona.setText(String.valueOf(c.getIdpersona()));
        txtnombre.setText(c.getNombre());
        txtapaterno.setText(c.getApaterno());
        txtamaterno.setText(c.getAmaterno());
        cbotipo_documento.setSelectedItem(c.getTipo_documento());
        txtnum_documento.setText(c.getNum_documento());
        txtdireccion.setText(c.getDireccion());
        txttelefono.setText(c.getTelefono());
        txtemail.setText(c.getEmail());
        txtcodigo_cliente.setText(c.getCodigo_cliente());
    }
    }//GEN-LAST:event_tablalistadoMouseClicked

