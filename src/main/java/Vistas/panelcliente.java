public void mostrar(String buscar) {
    clienteController controlador = new clienteController();
    listaClientes = controlador.mostrar(buscar);

    // Definir columnas
    String[] columnas = {"ID", "Nombre", "Apellido P.", "Apellido M.", "Tipo Doc", "Número Doc", "Dirección", "Teléfono", "Email", "Código Cliente"};

    // Crear modelo con columnas y filas vacías
    DefaultTableModel modelo = new DefaultTableModel(null, columnas);

    // Llenar filas
    for (cliente c : listaClientes) {
        Object[] fila = {
            c.getIdpersona(),
            c.getNombre(),
            c.getApaterno(),
            c.getAmaterno(),
            c.getTipo_documento(),
            c.getNum_documento(),
            c.getDireccion(),
            c.getTelefono(),
            c.getEmail(),
            c.getCodigo_cliente()
        };
        modelo.addRow(fila);
    }

    // Asignar modelo a la tabla
    tablalistado.setModel(modelo);
}


    
