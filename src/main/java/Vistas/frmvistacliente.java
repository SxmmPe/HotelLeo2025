    private void tablalistadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMousePressed
         if (evt.getClickCount() == 2) {
        int fila = tablalistado.getSelectedRow();

        if (fila != -1) {
            String cod = tablalistado.getValueAt(fila, 0).toString();
            String nombre = tablalistado.getValueAt(fila, 1).toString();
            String apaterno = tablalistado.getValueAt(fila, 2).toString();

            String valor = nombre + " " + apaterno;

            // Verifica que panelreserva no sea null antes de usarlo
            try {
                panelreserva.txtidcliente.setText(cod);
                panelreserva.txtcliente.setText(valor);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No se pudo enviar los datos al formulario de reserva.\n" + e.getMessage());
            }

            this.dispose();
        }
    }
    }//GEN-LAST:event_tablalistadoMousePressed

