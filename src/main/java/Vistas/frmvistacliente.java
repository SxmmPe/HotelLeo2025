void mostrar(String buscar) {
    try {
        clienteController func = new clienteController();
        java.util.List<Modelo.cliente> lista = func.mostrar(buscar);

        // Definir las columnas de la tabla
        String[] titulos = {
            "ID", "Nombre", "Apellido Paterno", "Apellido Materno", 
            "Tipo Doc", "N° Documento", "Dirección", 
            "Teléfono", "Email", "Código Cliente"
        };

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        // Agregar filas al modelo
        for (Modelo.cliente c : lista) {
            String[] fila = new String[10];
            fila[0] = String.valueOf(c.getIdpersona());
            fila[1] = c.getNombre();
            fila[2] = c.getApaterno();
            fila[3] = c.getAmaterno();
            fila[4] = c.getTipo_documento();
            fila[5] = c.getNum_documento();
            fila[6] = c.getDireccion();
            fila[7] = c.getTelefono();
            fila[8] = c.getEmail();
            fila[9] = c.getCodigo_cliente();

            modelo.addRow(fila);
        }

        tablalistado.setModel(modelo);
        ocultar_columnas();
        lbltotalregistros.setText("Total Registros: " + func.totalregistros);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, e);
    }
}

    

