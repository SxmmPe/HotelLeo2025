            String valor;
            
            cod=tablalistado.getValueAt(fila, 0).toString();
            valor=tablalistado.getValueAt(fila, 1).toString();
            
            panelreserva.txtidhabitacion.setText(cod);
            panelreserva.txtnumero.setText(valor);
            this.dispose();
            
