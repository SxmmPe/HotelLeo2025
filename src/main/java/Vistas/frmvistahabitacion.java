void mostrar(String buscar) {
    try {
        habitacionController func = new habitacionController();
        List<habitacion> lista = func.mostrarvista(buscar);


        String[] titulos = {"Piso", "Número", "Descripción", "Precio Diario"};
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados.");
        }

        for (habitacion h : lista) {
            modelo.addRow(new Object[]{
                h.getPiso(),
                h.getNumero(),
                h.getDescripcion(),
                h.getPrecio_diario()
            });
        }

        tablalistado.setModel(modelo);
        lbltotalregistros.setText("Total Registros: " + lista.size());

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
private void buscarAutomaticamente() {
    String texto = txtbuscar.getText().trim();
    mostrar(texto); 
}

    
 
    

