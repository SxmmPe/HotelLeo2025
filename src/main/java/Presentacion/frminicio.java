    private void btnBackupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackupActionPerformed
       try {

        File carpeta = new File("C:\\backups");
        if (!carpeta.exists()) {
            carpeta.mkdirs();
        }

       
        String fecha = new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());

     
        String rutaBackup = "C:\\backups\\hotel_backup_MANUAL" + fecha + ".sql";

      
        String comando = "mysqldump -u root hotel -r \"" + rutaBackup + "\"";

 
        Process proceso = Runtime.getRuntime().exec("cmd /c " + comando);
        int resultado = proceso.waitFor();

 
        if (resultado == 0) {
            JOptionPane.showMessageDialog(null, "Backup realizado exitosamente:\n" + rutaBackup);
        } else {
            JOptionPane.showMessageDialog(null, "Error al realizar el backup.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "OcurriÃ³ un error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnBackupActionPerformed

